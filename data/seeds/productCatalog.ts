import type { Product, UserProblem } from "@/lib/recommendation/types";
import type { CatalogProduct } from "@/lib/catalog/catalogTypes";

export type ProductModelCategory =
  | "laptop"
  | "monitor"
  | "laptop_stand"
  | "keyboard"
  | "mouse"
  | "chair"
  | "desk_lamp"
  | "headphones"
  | "webcam";

export type ProductModelPriceBand = "budget" | "mid" | "premium";
export type ProductModelNoiseLevel = "quiet" | "normal" | "loud";
export type ProductModelFeatures = Record<string, string | number | boolean | string[]>;

export interface ProductModel {
  id: string;
  category: ProductModelCategory;
  brand: string;
  model: string;
  displayName: string;
  gtin?: string;
  upc?: string;
  priceBand: ProductModelPriceBand;
  estimatedPriceCents: number;
  features: ProductModelFeatures;
  bestFor: string[];
  badFor: string[];
  solvesProblems: string[];
  constraints: {
    minBudgetCents?: number;
    maxDeskWidthInches?: number;
    requiredPorts?: string[];
    noiseLevel?: ProductModelNoiseLevel;
    accessibilityTags?: string[];
  };
  scoringTags: {
    productivity: number;
    ergonomics: number;
    portability: number;
    value: number;
    comfort: number;
    accessibility: number;
    focus: number;
    performance: number;
  };
  searchQueries: string[];
}

export type ProductCatalogItem = ProductModel & Product;

function defineProduct(product: ProductModel): ProductCatalogItem {
  const portable = Boolean(product.features.portable || product.features.foldable || product.scoringTags.portability >= 7);
  const quiet = product.constraints.noiseLevel === "quiet";

  return {
    ...product,
    name: product.displayName,
    priceUsd: Math.round(product.estimatedPriceCents / 100),
    shortDescription: product.bestFor.join("; ") + ".",
    strengths: product.bestFor,
    solves: product.solvesProblems as UserProblem[],
    aspirational: product.priceBand === "premium",
    constraints: {
      ...product.constraints,
      minDeskWidthInches: product.constraints.maxDeskWidthInches,
      portable,
      quiet,
    },
    scoreHints: {
      comfort: product.scoringTags.comfort,
      productivity: product.scoringTags.productivity,
      accessibility: product.scoringTags.accessibility,
      value: product.scoringTags.value,
    },
  };
}

